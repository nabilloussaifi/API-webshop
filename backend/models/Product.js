const mongoose = require('mongoose');
const ProductSchema = new mongoose.Schema({
    type: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    img: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    rating: {
        type: Number,
        required: true
    },
    countInStock: {
        type: Number,
        required: true
    }

});

module.exports = mongoose.model('Product', ProductSchema);